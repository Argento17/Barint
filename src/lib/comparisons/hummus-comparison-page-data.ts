export type HummusCorpusMeta = ComparisonCorpusMeta;

type HummusCorpusProduct = BariProductVM & {
  _product_type?: string;
};

// NOVA-1 single-ingredient whole/raw/frozen chickpea products. These score highly
// because they are minimally processed, but they are not comparable to prepared
// hummus spreads. Excluded from ranked display for v1.
// See excluded_products_hummus_v1.md for full audit record.
const EXCLUDED_NOVA1_IDS = new Set([
  "bsip1_7296073733324", // חומוס — 100% גרגרי חומוס, NOVA-1
  "bsip1_7296073733331", // חומוס ענק — גרגרי חומוס ענק, NOVA-1
  "bsip1_7296073005889", // חומוס לבן ענק שופרסל — NOVA-1
  "bsip1_7296073006015", // חומוס גדול שופרסל — NOVA-1
  "bsip1_3643820",       // חומוס ענק — NOVA-1
  "bsip1_7296073705505", // חומוס מוקפא — גרגרים קפואים, NOVA-1
]);

// TASK-086: generic grade-level fallback text was REMOVED. Per the BSIP2 → Web
// Translation Contract, a scored product must never rely on per-grade boilerplate
// such as "פרופיל הרכב טוב ביחס לקטגוריה". Each product now carries a
// product-specific insightLine plus deterministically-derived explanation arrays
// (positiveSignals / limitingFactors / unknowns / caveats) generated by
// build_hummus_explanation_v1.py from its BSIP2 trace. The UI renders those
// verbatim; this layer only strips the internal _product_type field.

function stripHummusInternalFields(products: HummusCorpusProduct[]): BariProductVM[] {
  return products
    .filter((product) => !EXCLUDED_NOVA1_IDS.has(product.id))
    .map((product) => {
      const { _product_type, ...rest } = product;
      void _product_type;
      return rest;
    });
}

const loaded = loadComparisonCorpus(rawCorpus as unknown as ComparisonCorpusRaw);
const hummusCorpusMeta = loaded.meta;
const hummusProducts = stripHummusInternalFields(
  loaded.products as HummusCorpusProduct[]
);

