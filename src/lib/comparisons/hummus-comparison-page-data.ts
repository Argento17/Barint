function isHummusShelfFilterId(filter: string): filter is HummusShelfFilterId {
  return HUMMUS_SHELF_LENS_OPTIONS.some((option) => option.id === filter);
}

const hummusShelfFilters = {
  lensOptions: HUMMUS_SHELF_LENS_OPTIONS,
  filterProducts: (products: BariProductVM[], activeFilters: string[]) =>
    filterHummusProducts(products, activeFilters.filter(isHummusShelfFilterId)),
};

export function getHummusPageData(): ComparisonCategoryPageData {
  return {
    products: hummusProducts,
    metadataLine: hummusMetadataLine,
    hero: hummusHero,
    prologueSentences: hummusPrologueSentences,
    methodologyLines: hummusMethodologyLines,
    corpusMeta: hummusCorpusMeta,
    shelfFilters: hummusShelfFilters,
  };
}

export function getHummusCorpusPayload(): {
  _meta: HummusCorpusMeta;
  products: BariProductVM[];
} {
  return {
    _meta: hummusCorpusMeta,
    products: hummusProducts,
  };
}
