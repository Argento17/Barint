def main():
    doc = json.loads(FE.read_text("utf-8"))
    products = doc["products"]
    seen, missing, overlong = set(), [], []
    for p in products:
        pid = p["id"]
        if pid not in COPY:
            missing.append((pid, p.get("name")))
            continue
        il, rv = COPY[pid]
        p["insightLine"] = il
        p["rowVerdict"] = rv
        seen.add(pid)
        if len(rv) > 185:
            overlong.append((pid, len(rv)))
    unused = set(COPY) - seen
    FE.write_text(json.dumps(doc, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"patched {len(seen)}/{len(products)} products")
    if missing:
        print("NO COPY FOR:", missing)
    if unused:
        print("UNUSED COPY KEYS (id not in file):", unused)
    if overlong:
        print("OVERLONG rowVerdict (>185 chars):", overlong)


if __name__ == "__main__":
    main()
