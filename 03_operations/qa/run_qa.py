def check_cov005(bsip1_files: list[tuple[pathlib.Path, dict]]) -> Check:
    """COV-005: Sodium and sugar coverage (informational WARN)."""
    chk = Check("COV-005", "Sodium / sugar coverage (informational)", verdict_type="warning")
    chk.total = len(bsip1_files)
    valid = [rec for _, rec in bsip1_files if not rec.get("_load_error")]
    n = len(valid)
    sodium = sum(1 for r in valid if (r.get("normalized_nutrition_per_100g") or {}).get("sodium_mg") is not None)
    sugar  = sum(1 for r in valid if (r.get("normalized_nutrition_per_100g") or {}).get("sugars_g") is not None)
    sodium_pct = (sodium / n * 100) if n else 0
    sugar_pct  = (sugar  / n * 100) if n else 0
    chk.notes.append(f"sodium_mg: {sodium}/{n} ({sodium_pct:.1f}%) — target ≥ 60%")
    chk.notes.append(f"sugars_g:  {sugar}/{n}  ({sugar_pct:.1f}%)  — target ≥ 50%")
    if sodium_pct < 60:
        chk.warn(f"sodium_mg coverage {sodium_pct:.1f}% below 60% — may reduce BSIP2 red-label detection quality")
    if sugar_pct < 50:
        chk.warn(f"sugars_g coverage {sugar_pct:.1f}% below 50% — may reduce BSIP2 sugar-score quality")
    return chk


def check_can001(
    canonical_dir: pathlib.Path,
    bsip0_approved_count: int,
) -> Check:
    """CAN-001: canonical_bsip1 exists and product count ≥ 90% of BSIP0 approved."""
    chk = Check("CAN-001", "canonical_bsip1/ exists and is populated")
    if not canonical_dir.exists():
        chk.fail(f"Directory {canonical_dir} does not exist")
        return chk
    files = list(canonical_dir.glob("bsip1_*.json"))
    chk.total = bsip0_approved_count
    canonical_count = len(files)
    chk.notes.append(f"canonical_bsip1/ contains {canonical_count} files; BSIP0 approved = {bsip0_approved_count}")
    if canonical_count == 0:
        chk.fail("canonical_bsip1/ is empty")
    elif bsip0_approved_count > 0:
        floor = int(bsip0_approved_count * 0.90)
        if canonical_count < floor:
            chk.fail(
                f"canonical count {canonical_count} < 90% of BSIP0 approved {bsip0_approved_count} "
                f"(floor = {floor}) — check BSIP1 enrichment log for errors"
            )
        elif canonical_count < bsip0_approved_count:
            chk.warn(
                f"canonical count {canonical_count} is {bsip0_approved_count - canonical_count} "
                f"below BSIP0 approved count — within 10% tolerance"
            )
    return chk


def check_can003(canonical_files: list[tuple[pathlib.Path, dict]]) -> Check:
    """CAN-003: schema_version consistent across all canonical files."""
    chk = Check("CAN-003", f"schema_version = '{APPROVED_SCHEMA_VERSION}' in all canonical files")
    chk.total = len(canonical_files)
    seen_versions: set[str] = set()
    for path, rec in canonical_files:
        if "_load_error" in rec:
            chk.fail("JSON load error", path.stem)
            continue
        sv = rec.get("schema_version")
        seen_versions.add(str(sv) if sv is not None else "ABSENT")
        if sv != APPROVED_SCHEMA_VERSION:
            chk.fail(f"schema_version = '{sv}' (expected '{APPROVED_SCHEMA_VERSION}')", path.stem)
    chk.notes.append(f"schema_version values found: {sorted(seen_versions)}")
    return chk


def check_can004(
    canonical_dir: pathlib.Path,
    rejected_codes: set[str],
) -> Check:
    """CAN-004: No excluded products leaked into canonical_bsip1/."""
    chk = Check("CAN-004", "No excluded products in canonical_bsip1/")
    chk.notes.append(f"{len(rejected_codes)} rejected codes from candidate_review.csv")
    if not canonical_dir.exists():
        chk.skip("canonical_bsip1/ directory does not exist — CAN-001 will report this")
        return chk
    for f in sorted(canonical_dir.glob("bsip1_*.json")):
        barcode = f.stem.replace("bsip1_", "")
        if barcode in rejected_codes:
            chk.fail(f"Excluded barcode {barcode} found in canonical_bsip1/", barcode)
    chk.total = len(list(canonical_dir.glob("bsip1_*.json")))
    return chk


def check_can005(canonical_files: list[tuple[pathlib.Path, dict]]) -> Check:
    """CAN-005: All canonical files have file_type = 'product'."""
    chk = Check("CAN-005", "All canonical files have file_type = 'product'")
    chk.total = len(canonical_files)
    seen: set[str] = set()
    for path, rec in canonical_files:
        if "_load_error" in rec:
            chk.fail("JSON load error", path.stem)
            continue
        ft = rec.get("file_type")
        seen.add(str(ft) if ft is not None else "ABSENT")
        if ft != "product":
            chk.fail(f"file_type = '{ft}' (expected 'product')", path.stem)
    chk.notes.append(f"file_type values found: {sorted(seen)}")
    return chk


def _check_forbidden_fields(
    canonical_files: list[tuple[pathlib.Path, dict]],
    forbidden: frozenset[str],
    check_id: str,
    name: str,
) -> Check:
    chk = Check(check_id, name)
    chk.total = len(canonical_files)
    all_found: set[str] = set()
    for path, rec in canonical_files:
        if "_load_error" in rec:
            chk.fail("JSON load error", path.stem)
            continue
        hits = forbidden & rec.keys()
        if hits:
            all_found.update(hits)
            chk.fail(f"Forbidden fields: {sorted(hits)}", path.stem)
    if all_found:
        chk.notes.append(f"Forbidden fields found across corpus: {sorted(all_found)}")
    return chk


