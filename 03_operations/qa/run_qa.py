# ── Report generator ───────────────────────────────────────────────────────────

def _pct(n: int, total: int) -> str:
    return f"{n}/{total} ({int(n / total * 100)}%)" if total else "0/0"


def generate_report(
    category: str,
    checks: list[Check],
    overall_pass: bool,
    run_ts: str,
) -> str:
    hard_fails = [c for c in checks if c.is_hard_fail]
    warnings   = [c for c in checks if c.result == "WARN"]
    passes     = [c for c in checks if c.result == "PASS"]
    skips      = [c for c in checks if c.result == "SKIP"]

    lines = [
        f"# QA Report — {category}",
        f"",
        f"**Generated:** {run_ts}",
        f"**Category:** {category}",
        f"**QA runner version:** run_qa.py v1",
        f"**Overall verdict:** {'PASS' if overall_pass else 'FAIL'}",
        f"",
        f"| Result | Count |",
        f"|--------|-------|",
        f"| PASS   | {len(passes)} |",
        f"| WARN   | {len(warnings)} |",
        f"| FAIL   | {len(hard_fails)} |",
        f"| SKIP   | {len(skips)} |",
        f"| Total  | {len(checks)} |",
        f"",
    ]

    if hard_fails:
        lines += [
            f"## Hard Fails — BSIP2 entry blocked",
            f"",
        ]
        for c in hard_fails:
            lines.append(f"### {c.id} — {c.name}")
            for note in c.notes:
                lines.append(f"- {note}")
            if c.affected_ids:
                sample = c.affected_ids[:10]
                suffix = f" (…and {len(c.affected_ids) - 10} more)" if len(c.affected_ids) > 10 else ""
                lines.append(f"- Affected products ({c.affected}): `{'`, `'.join(sample)}`{suffix}")
            lines.append("")

    if warnings:
        lines += [
            f"## Warnings — proceed with documented note",
            f"",
        ]
        for c in warnings:
            lines.append(f"### {c.id} — {c.name}")
            for note in c.notes:
                lines.append(f"- {note}")
            if c.affected_ids:
                sample = c.affected_ids[:10]
                suffix = f" (…and {len(c.affected_ids) - 10} more)" if len(c.affected_ids) > 10 else ""
                lines.append(f"- Affected products ({c.affected}): `{'`, `'.join(sample)}`{suffix}")
            lines.append("")

    lines += [
        f"## All Check Results",
        f"",
        f"| Check | Name | Result | Total | Affected |",
        f"|-------|------|--------|-------|----------|",
    ]
    for c in checks:
        lines.append(
            f"| {c.id} | {c.name} | {c.icon} | {c.total or '—'} | {c.affected or '—'} |"
        )

    lines += [
        f"",
        f"## Check Details",
        f"",
    ]
    for c in checks:
        lines.append(f"### {c.id} — {c.icon} — {c.name}")
        for note in c.notes:
            lines.append(f"- {note}")
        if c.affected_ids:
            sample = c.affected_ids[:20]
            suffix = f" (…and {len(c.affected_ids) - 20} more)" if len(c.affected_ids) > 20 else ""
            lines.append(f"- Affected ({c.affected}): `{'`, `'.join(sample)}`{suffix}")
        if not c.notes and not c.affected_ids:
            lines.append("- No issues found.")
        lines.append("")

    lines += [
        f"## Skipped Checks (manual or pipeline not yet ready)",
        f"",
        f"| Check | Reason |",
        f"|-------|--------|",
        f"| SCP-001 | corpus_filter.md lock marker — manual verification |",
        f"| SCP-002 | candidate_review.csv — covered by CAN-004 path resolution |",
        f"| SCP-003 | OOS term matching — requires category-specific term injection |",
        f"| SCP-004 | Count vs expected range — requires corpus_filter.md threshold parsing |",
        f"| SCP-005 | Multi-pack / catering size — weight_g unreliable from scraper |",
        f"| TRC-005 | audit_ref — audit files not yet generated by pipeline |",
        f"| COV-004 | Barcode quality ratio — informational only |",
        f"| CAN-002 | Manifest file — manifest generator not yet in pipeline (Wave 2) |",
        f"| REG-004 | Unexpected top-level fields — WARN only; bsip0_source is a known extra |",
        f"| REG-005 | BSIP0 mtime check — fragile across OS copy operations |",
        f"",
        f"---",
        f"",
        f"*Report generated by `run_qa.py` v1 — Bari Category Factory QA Runner*",
        f"*Reference: `03_operations/qa/category_factory_qa_v1.md`*",
    ]

    return "\n".join(lines)


# ── Main ───────────────────────────────────────────────────────────────────────

def main() -> int:
    parser = argparse.ArgumentParser(
        description="Bari Category Factory QA Runner v1"
    )
    parser.add_argument("--category", required=True, help="Category slug, e.g. hummus")
    parser.add_argument(
        "--retailer",
        default="shufersal",
        help="Primary retailer for BSIP0 observations (default: shufersal)",
    )
    parser.add_argument(
        "--bsip1-run",
        dest="bsip1_run",
        default=None,
        help="BSIP1 run ID (default: run_{category}_001)",
    )
    args = parser.parse_args()

    category  = args.category
    retailer  = args.retailer
    run_id    = args.bsip1_run or f"run_{category}_001"
    run_ts    = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")

    # ── Resolve paths ──────────────────────────────────────────────────────────
    product_dir   = PRODUCTS_ROOT / category
    canonical_dir = product_dir / "canonical_bsip1"
    obs_root      = product_dir / "observations_bsip0"
    obs_retailer  = obs_root / retailer
    bsip1_run_dir = BSIP1_OPS_ROOT / run_id / "output"
    review_csv    = obs_retailer / "candidate_review.csv"

    print(f"Bari Category Factory QA Runner v1")
    print(f"Category : {category}")
    print(f"Retailer : {retailer}")
    print(f"BSIP1 run: {run_id}")
    print()

    # ── Check directories exist ────────────────────────────────────────────────
    path_issues = []
    if not product_dir.exists():
        path_issues.append(f"Product directory not found: {product_dir}")
    if not obs_retailer.exists():
        path_issues.append(f"BSIP0 observations not found: {obs_retailer}")
    if path_issues:
        for issue in path_issues:
            print(f"ERROR: {issue}")
        return 1

    # ── Load data ──────────────────────────────────────────────────────────────
    # BSIP1 run output (for TRC/COV checks)
    if bsip1_run_dir.exists():
        bsip1_run_files = _load_json_dir(bsip1_run_dir)
    else:
        print(f"WARN: BSIP1 run directory not found: {bsip1_run_dir}")
        print(f"      Falling back to canonical_bsip1/ for TRC/COV checks.")
        bsip1_run_files = _load_json_dir(canonical_dir) if canonical_dir.exists() else []

    canonical_files = _load_json_dir(canonical_dir) if canonical_dir.exists() else []

    bsip0_files = _load_bsip0_obs(obs_retailer)
    bsip0_approved_count = len(bsip0_files)

    rejected_codes = _rejected_codes_from_csv(review_csv)

    print(f"BSIP0 approved observations : {bsip0_approved_count}")
    print(f"BSIP1 run output files      : {len(bsip1_run_files)}")
    print(f"canonical_bsip1 files       : {len(canonical_files)}")
    print(f"Rejected codes from CSV     : {len(rejected_codes)}")
    print()

    # ── Run checks ─────────────────────────────────────────────────────────────
    checks: list[Check] = []

    print("Running checks ...")

    c = check_trc001(bsip1_run_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_trc002(bsip1_run_files, obs_root)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_trc003(bsip1_run_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_trc004(bsip0_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_cov001(bsip1_run_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_cov002(bsip1_run_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_cov003(bsip1_run_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_cov005(bsip1_run_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_can001(canonical_dir, bsip0_approved_count)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_can003(canonical_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_can004(canonical_dir, rejected_codes)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_can005(canonical_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_reg001(canonical_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_reg002(canonical_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    c = check_reg003(canonical_files)
    checks.append(c)
    print(f"  {c.id:8}  {c.icon}")

    # ── Verdict ────────────────────────────────────────────────────────────────
    hard_fails = [c for c in checks if c.is_hard_fail]
    warnings   = [c for c in checks if c.result == "WARN"]
    overall_pass = len(hard_fails) == 0

    print()
    print(f"{'='*50}")
    print(f"PASS: {sum(1 for c in checks if c.result == 'PASS')}  "
          f"WARN: {len(warnings)}  "
          f"FAIL: {len(hard_fails)}  "
          f"SKIP: {sum(1 for c in checks if c.result == 'SKIP')}")
    print()
    if overall_pass:
        print(f"VERDICT: PASS — {category} is clear for BSIP2 entry")
        if warnings:
            print(f"         ({len(warnings)} warning(s) — see report for details)")
    else:
        print(f"VERDICT: FAIL — {len(hard_fails)} Hard Fail(s) must be resolved before BSIP2")
        for c in hard_fails:
            print(f"  * {c.id}: {c.name}")
    print(f"{'='*50}")

    # ── Write report ───────────────────────────────────────────────────────────
    REPORTS_DIR.mkdir(parents=True, exist_ok=True)
    report_path = REPORTS_DIR / f"qa_report_{category}.md"
    report_text = generate_report(category, checks, overall_pass, run_ts)
    report_path.write_text(report_text, encoding="utf-8")
    print(f"\nReport: {report_path}")

    return 0 if overall_pass else 1


if __name__ == "__main__":
    sys.exit(main())
